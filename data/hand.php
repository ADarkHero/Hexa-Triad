<?php

    class hand {

        //Saves Card Objects
        var $hand = array();

/**********************************************************************************************************************
* Creates a hand of cards out of card objects
**********************************************************************************************************************/
        function __construct($cards, $temp){
            for($i = 1; $i <= sizeof($cards); $i++){
                 $card = new card($cards[$i], $temp);
                 $this->hand[$i] = $card;
            }
        }

/**********************************************************************************************************************
* Display player hands
**********************************************************************************************************************/
        function showHand(){
            for($i = 1; $i <= sizeof($this->hand); $i++){
                $card = $this->hand[$i];
                $card->displaycard();
            }
        }

        function showYourHand($id){
            for($i = 1; $i <= sizeof($this->hand); $i++){
                $card = $this->hand[$i];
                $card->displayyourcard($id);
            }
        }



/**********************************************************************************************************************
* Counts cards in hand
**********************************************************************************************************************/
        function countHand(){
            $counter = 0;
            for($i = 1; $i <= sizeof($this->hand); $i++){
                $card = $this->hand[$i];
                if($card->cardid != 0){
                    $counter++;
                }
            }
            return $counter;
        }



/**********************************************************************************************************************
* Search Hand
**********************************************************************************************************************/
        function searchHand($cardid){
            for($i = 1; $i <= sizeof($this->hand); $i++){
                $card = $this->hand[$i];
                if($card->cardid == $cardid){
                    return $i;
                }
            }
            return false;
        }


/**********************************************************************************************************************
* Display cards on field
**********************************************************************************************************************/
        function showField($id, $cardid){
            $x = 0;
            $y = 0;

            for($i = 1; $i <= sizeof($this->hand); $i++){
                $card = $this->hand[$i];
                $mod = $i-1;
                if($mod%6 == 0 && $mod != 0){
                    $y += 150;
                    $x = 0;
                }
                $card->displayfieldcard($id, $cardid, $i, $x, $y);

                $x += 100;
            }
        }



    }

?>